let std_marks;
std_marks=77;

//if/if-else/if-elseif
//using else if for multiple conditions

if(std_marks>=90)
{
    document.write("You have passed with A grade!");
}
else if(std_marks>=80)
{
    document.write("You have passed with B grade!");
}
else if(std_marks>=70)
{
    document.write("You have passed with C grade!");
}
else if(std_marks>=60)
{
    document.write("You have passed with D grade!");
}
else 
{
    document.write("sorry you are failed!");
}

